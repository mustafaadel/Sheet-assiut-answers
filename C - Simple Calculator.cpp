#include <bits/stdc++.h>
using namespace std;

int main() {
    ios::sync_with_stdio(0);cin.tie(0);
    ios_base::sync_with_stdio(0);
   long long x , y, sum , multi , sub;
   cin >> x >> y ;
   sum = x + y ;
   multi = x*y ;
   sub = x - y ;
   cout << x << " + " << y << " = " << sum << endl;
   cout << x << " * " << y << " = " << multi << endl;
   cout << x << " - " << y << " = " << sub << endl;

}
